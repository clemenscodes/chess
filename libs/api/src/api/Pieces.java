package api;

/**
 * The {@link Pieces} enum represents the different types of chess pieces.
 * Each enum constant corresponds to a specific chess piece in either black or
 * white color.
 */
public enum Pieces {
    /**
     * Represents the black rook chess piece.
     */
    BlackRook,

    /**
     * Represents the black knight chess piece.
     */
    BlackKnight,

    /**
     * Represents the black bishop chess piece.
     */
    BlackBishop,

    /**
     * Represents the black queen chess piece.
     */
    BlackQueen,

    /**
     * Represents the black king chess piece.
     */
    BlackKing,

    /**
     * Represents the black pawn chess piece.
     */
    BlackPawn,

    /**
     * Represents the white rook chess piece.
     */
    WhiteRook,

    /**
     * Represents the white knight chess piece.
     */
    WhiteKnight,

    /**
     * Represents the white bishop chess piece.
     */
    WhiteBishop,

    /**
     * Represents the white queen chess piece.
     */
    WhiteQueen,

    /**
     * Represents the white king chess piece.
     */
    WhiteKing,

    /**
     * Represents the white pawn chess piece.
     */
    WhitePawn,
}
